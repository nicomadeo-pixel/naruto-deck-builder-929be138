export type CardType = "character" | "mission";
export type Rarity = "C" | "UC" | "R" | "SR" | "SecretV" | "Legendary";
export type Faction = "Konoha" | "Sound" | "Sand" | "Akatsuki" | "Neutral";
export type EffectType = "Instant" | "Continuous" | "Ambush" | "none";

export interface CharacterCard {
  id: string;
  type: "character";
  name: string;
  version: string;
  chakraCost: number;
  power: number;
  faction: Faction;
  keywords: string[];
  effect: string;
  effectType: EffectType;
  rarity: Rarity;
  imageUrl: string;
  cardNumber: string;
}

export interface MissionCard {
  id: string;
  type: "mission";
  title: string;
  missionPoints: number;
  effect: string;
  imageUrl: string;
  cardNumber: string;
}

